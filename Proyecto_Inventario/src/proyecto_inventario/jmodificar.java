        TFdescmod.setEnabled(true);
        TFdescmod.setText(String.valueOf(SharedData.datos.getListaprod().get(index).getDesc()));
        
        TFunimod.setEnabled(true);
        TFunimod.setText(String.valueOf(SharedData.datos.getListaprod().get(index).getUnidad()));
        
        TFpcompramod.setEnabled(true);
